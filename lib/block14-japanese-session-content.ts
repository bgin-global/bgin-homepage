export function getSessionContentJP(sessionId: string): SessionContentJP | null {
  const summary = sessionSummariesJP[sessionId];
  const agenda = sessionAgendasJP[sessionId];
  if (!summary && !agenda) return null;
  return {
    summary: summary ?? "",
    agenda: agenda ?? [],
  };
}

/**
 * 日本語版で表示するセッション概要を返す
 * JP コンテンツがあればそれを使用、なければ英語をそのまま返す
 */
export function getDisplaySummaryJP(sessionId: string, englishSummary: string): string {
  const jp = getSessionContentJP(sessionId);
  if (jp && jp.summary) return jp.summary;
  return englishSummary;
}

/**
 * 日本語版で表示するアジェンダを返す
 * JP コンテンツにアジェンダがあればそれを使用、なければ英語をそのまま返す
 */
export function getDisplayAgendaJP(
  sessionId: string,
  englishAgenda: string[]
): string[] {
  const jp = getSessionContentJP(sessionId);
  if (jp && jp.agenda && jp.agenda.length > 0) return jp.agenda;
  return englishAgenda;
}
